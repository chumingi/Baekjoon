""" 입력
첫째 줄: 알파벳 소문자로만 이루어진 단어 S 입력 (S의 길이 <= 100)"""
""" 출력
S에 포함된 a~z의 개수를 공백으로 구분하여 출력"""

S = input().strip()
alphabets = [0] * 26

for c in S:
    alphabets[ord(c) - ord('a')] += 1
print(*alphabets)

""" 시간복잡도: O(N)
- N: 입력 문자열 길이 (최대 100)
- 각 문자를 순회하며 리스트에 접근 (O(1) 연산)"""
""" 공간복잡도: O(1)
- 고정 크기 리스트 26개만 사용
- 입력 문자열 외에 추가 메모리는 상수 크기"""