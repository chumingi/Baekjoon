""" 입력
첫째 줄: 정수의 개수 N(1 ≤ N ≤ 100)
둘째 줄: 정수가 공백으로 구분되어 있음
셋째 줄: 찾으려고 하는 정수 v (-100 <= v <= 100)"""
""" 출력
첫째 줄: 입력으로 주어진 N개의 정수 중에 v가 몇 개인지 출력"""

N = int(input())
arr = list(map(int, input().split()))
v = int(input())

print(arr.count(v))

""" 시간복잡도: O(N)
- arr.count(v)는 리스트 전체를 순회하며 v와 같은 원소의 개수를 셈
- N은 최대 100이므로 최악의 경우에도 100번 비교 → O(N)"""
""" 공간복잡도: O(N)
- 입력 받은 수열을 저장하는 arr 배열의 크기만큼 공간이 필요함
- 별도의 추가 공간은 사용하지 않음 → O(N)"""